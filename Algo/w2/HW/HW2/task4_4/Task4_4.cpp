#include <iostream>
using namespace std;


int GetPivot(int min, int max) {
	int variable_index = rand() % (max - min + 1) + min;
	return variable_index;
}

int Partition(int* arr, int l, int r) {
	int p = GetPivot(l, r);
	if (l != r)
		std::swap(arr[p], arr[l]);
	int x = arr[l];
	int i = r+1;
	for (int j = r; j >= l; j--) {
		if (arr[j] >= x)
			std::swap(arr[--i], arr[j]);
	}
	return i;
}


int KStat(int* arr, int n, int k) {
	int left = 0;
	int right = n - 1;
	while (true) {
		int p = Partition(arr, left, right);
		if (p < k)
			left = p + 1;
		else
			if (p > k)
				right = p - 1;
			else
				return k;
	}
}

int main() {
	int n, k;
	cin >> n >> k;
	int* arr = new int[n];
	for (int i = 0; i < n; i++) {
		cin >> arr[i];
	}

	int k_ind = KStat(arr, n, k);
	cout << arr[k_ind];

	delete[] arr;
	return 0;
}